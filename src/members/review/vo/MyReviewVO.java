package members.review.vo;

import java.text.SimpleDateFormat;
import java.util.Date;

public class MyReviewVO {
	private String rev_id;
	private String game_id;
	private String mem_id;
	private String game_name;
	private String game_img;
	private String rev_tit;
	private String rev_con;
	private Date rev_dt;
	private int rev_grd;
	
	public MyReviewVO() {
		super();
	}
	
	public MyReviewVO(String rev_id, String game_id, String game_name, String game_img, String rev_tit, String rev_con,
			Date rev_dt, int rev_grd) {
		super();
		this.rev_id = rev_id;
		this.game_id = game_id;
		this.game_name = game_name;
		this.game_img = game_img;
		this.rev_tit = rev_tit;
		this.rev_con = rev_con;
		this.rev_dt = rev_dt;
		this.rev_grd = rev_grd;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getRev_id() {
		return rev_id;
	}
	public void setRev_id(String rev_id) {
		this.rev_id = rev_id;
	}
	public String getGame_id() {
		return game_id;
	}
	public void setGame_id(String game_id) {
		this.game_id = game_id;
	}
	public String getGame_name() {
		return game_name;
	}
	public void setGame_name(String game_name) {
		this.game_name = game_name;
	}
	public String getGame_img() {
		return game_img;
	}
	public void setGame_img(String game_img) {
		this.game_img = game_img;
	}
	public String getRev_tit() {
		return rev_tit;
	}
	public void setRev_tit(String rev_tit) {
		this.rev_tit = rev_tit;
	}
	public String getRev_con() {
		return rev_con;
	}
	public void setRev_con(String rev_con) {
		this.rev_con = rev_con;
	}
	public Date getRev_dt() {
		return rev_dt;
	}
	public void setRev_dt(Date rev_dt) {
		this.rev_dt = rev_dt;
	}
	public int getRev_grd() {
		return rev_grd;
	}
	public void setRev_grd(int rev_grd) {
		this.rev_grd = rev_grd;
	}
	
	
}
